package lib.exceptions;

public class OpenReportException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public OpenReportException() {
	}

	public OpenReportException(String arg0) {
		super(arg0);
	}

	public OpenReportException(Throwable arg0) {
		super(arg0);
	}

	public OpenReportException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public OpenReportException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
